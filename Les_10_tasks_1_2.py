# Это желторотый питон по кличке "Каа". Возраст питомца: 19 лет. Имя
# владельца: Саша

pets = {
    "Каа": {
        'Вид питомца': 'Желторотый питон',
        'Возраст питомца': 19,
        'Имя владельца': 'Саша',
    }
}
