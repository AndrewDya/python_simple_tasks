# Задание N1. Вам необходимо создать словарь pets = {} Примерный вид будет следующим:
# pets = {"Имя питомца": {'Вид питомца': '','Возраст питомца': '','Имя владельца': ''}
# У вас должен получиться словарь, с ещё одним словарём внутри. То есть, есть словарь pets.
# Он в себе хранит ещё один словарь, который обозначается именем питомца. Имя питомца также
# нужно каким-то образом вносить туда.Задача не будет считаться выполненной, если вы заходите
# сразу внести информацию, не прибегая в функции input()
# Например: pets = {"Мухтар": {"Вид питомца": "Собака","Возраст питомца": 9,"Имя владельца": "Павел" }}
# Так должен будет выглядеть результируюший словарь,но первоначальный его вид - пустой.
# Его необходимо заполнить пользовательским вводом через консоль
# с помощью функции input(), а не вписать значения уже в самом коде.Возраст питомца должен быть типа int.
# Всё остальное - строки. Так как возраст питомца указывается типом int. Необходимо, в соответствии с указанным возрастом выводит год,
# года или лет. Например:Его возраст: 24 года. Его возраст: 21 год. Его возраст: 19 лет. И теперь осталось только
# получить всю информацию о питомце в виде строки, как из задания по Урок
# No3. Ввод-вывод и базовые переменные.


pets = {
    "Имя питомца": {
        'Вид питомца': '',
        'Возраст питомца': '',
        'Имя владельца': '',
    }
}
pets["Имя питомца"]["Вид питомца"] = input("Введите вид питомца: ")
pets["Имя питомца"]["Возраст питомца"] = int(
    input("Введите возраст питомца: "))
pets["Имя питомца"]["Имя владельца"] = input("Введите имя владельца: ")
new_name = input("Введите имя питомца: ")
pets[new_name] = pets.pop("Имя питомца")
print(pets)
if pets[new_name]["Возраст питомца"] == 1 or pets[new_name]["Возраст питомца"] == 21:
    age = 'год'
elif 1 < pets[new_name]["Возраст питомца"] < 5 or 21 < pets[new_name]["Возраст питомца"] < 25:
    age = 'года'
elif 4 < pets[new_name]["Возраст питомца"] < 21 or 24 < pets[new_name]["Возраст питомца"] < 31:
    age = 'лет'
print(f"Это {pets[new_name]['Вид питомца']} по кличке {new_name}. Возраст: {pets[new_name]['Возраст питомца']} {age}")

